    return pw.Container(
      child: pw.Column(
        crossAxisAlignment: pw.CrossAxisAlignment.start,
        children: [
          pw.Text(
            'Tips Menabung',
            style: pw.TextStyle(fontSize: 16, fontWeight: pw.FontWeight.bold),
          ),
          pw.SizedBox(height: 10),
          ...report.tips.map(
            (tip) => pw.Container(
              margin: const pw.EdgeInsets.only(bottom: 5),
              child: pw.Row(
                crossAxisAlignment: pw.CrossAxisAlignment.start,
                children: [
                  pw.Text(tip.icon, style: const pw.TextStyle(fontSize: 12)),
                  pw.SizedBox(width: 8),
                  pw.Expanded(
                    child: pw.Text(
                      tip.tip,
                      style: const pw.TextStyle(fontSize: 11),
                    ),
                  ),
                ],
              ),
            ),
          ),
        ],
      ),
    );
  }

  static pw.Widget _buildPdfMonthlyBreakdown(SavingsReport report) {
    return pw.Container(
      child: pw.Column(
        crossAxisAlignment: pw.CrossAxisAlignment.start,
        children: [
          pw.Text(
            'Tren Tabungan Bulanan',
            style: pw.TextStyle(fontSize: 16, fontWeight: pw.FontWeight.bold),
          ),
          pw.SizedBox(height: 10),
          pw.Table(
            border: pw.TableBorder.all(color: PdfColors.grey300),
            columnWidths: {
              0: const pw.FlexColumnWidth(2),
              1: const pw.FlexColumnWidth(3),
              2: const pw.FlexColumnWidth(1.5),
            },
            children: [
              pw.TableRow(
                decoration: const pw.BoxDecoration(color: PdfColors.grey200),
                children: [
                  _buildPdfTableCell('Bulan', isHeader: true),
                  _buildPdfTableCell('Total Ditabung', isHeader: true),
                  _buildPdfTableCell('Transaksi', isHeader: true),
                ],
              ),
              ...report.monthlyBreakdown.map(
                (item) => pw.TableRow(
                  children: [
                    _buildPdfTableCell(item.month),
                    _buildPdfTableCell(currencyFormatter.format(item.amount)),
                    _buildPdfTableCell('${item.depositCount}x'),
                  ],
                ),
              ),
            ],
          ),
        ],
      ),
    );
  }

  // ===== EXCEL EXPORT =====

  static Future<File> exportToExcel(SavingsReport report) async {
    final excel = Excel.createExcel();

    // Remove default sheet
    excel.delete('Sheet1');

    // Create Summary Sheet
    _createSummarySheet(excel, report);

    // Create Goals Sheet
    _createGoalsSheet(excel, report);

    // Create Monthly Breakdown Sheet
    if (report.monthlyBreakdown.isNotEmpty) {
      _createMonthlySheet(excel, report);
    }

    // Create Transactions Sheet
    if (report.transactions.isNotEmpty) {
      _createTransactionsSheet(excel, report);
    }

    // Create Achievements Sheet
    if (report.achievements.isNotEmpty) {
      _createAchievementsSheet(excel, report);
    }

    // Create Badges Sheet
    if (report.badges.isNotEmpty) {
      _createBadgesSheet(excel, report);
    }

    // Save file
    final directory = await getApplicationDocumentsDirectory();
    final timestamp = DateFormat('yyyyMMdd_HHmmss').format(DateTime.now());
    final file = File('${directory.path}/Laporan_GoalMoney_$timestamp.xlsx');
    await file.writeAsBytes(excel.save()!);

    return file;
  }

  static void _createSummarySheet(Excel excel, SavingsReport report) {
    final sheet = excel['Ringkasan'];

    // Header styling
    final headerStyle = CellStyle(
      bold: true,
      backgroundColorHex: ExcelColor.green300,
      fontColorHex: ExcelColor.white,
      fontSize: 14,
    );

    final labelStyle = CellStyle(bold: true);

    // Title
    sheet.cell(CellIndex.indexByString('A1')).value = TextCellValue(
      'LAPORAN PROGRESS TABUNGAN GOALMONEY',
    );
    sheet.cell(CellIndex.indexByString('A1')).cellStyle = headerStyle;
    sheet.merge(CellIndex.indexByString('A1'), CellIndex.indexByString('D1'));

    // Report info
    sheet.cell(CellIndex.indexByString('A3')).value = TextCellValue(
      'Tanggal Laporan:',
    );
    sheet.cell(CellIndex.indexByString('A3')).cellStyle = labelStyle;
    sheet.cell(CellIndex.indexByString('B3')).value = TextCellValue(
      report.reportDate,
    );

    sheet.cell(CellIndex.indexByString('A4')).value = TextCellValue('Periode:');
    sheet.cell(CellIndex.indexByString('A4')).cellStyle = labelStyle;
    sheet.cell(CellIndex.indexByString('B4')).value = TextCellValue(
      '${report.period.startDate} - ${report.period.endDate}',
    );

    // Summary section
    sheet.cell(CellIndex.indexByString('A6')).value = TextCellValue(
      'RINGKASAN',
    );
    sheet.cell(CellIndex.indexByString('A6')).cellStyle = headerStyle;
    sheet.merge(CellIndex.indexByString('A6'), CellIndex.indexByString('B6'));

    final summaryData = [
      ['Total Goals', report.summary.totalGoals],
      ['Goals Selesai', report.summary.completedGoals],
      ['Goals Aktif', report.summary.activeGoals],
      ['Total Ditabung', currencyFormatter.format(report.summary.totalSaved)],
      ['Total Target', currencyFormatter.format(report.summary.totalTarget)],
      [
        'Sisa Target',
        currencyFormatter.format(
          report.summary.totalTarget - report.summary.totalSaved,
        ),
      ],
      [
        'Progress Keseluruhan',
        '${report.summary.overallProgress.toStringAsFixed(1)}%',
      ],
      ['Total Deposit', report.summary.totalDeposits],
      ['Total Withdrawal', report.summary.totalWithdrawals],
    ];

    for (var i = 0; i < summaryData.length; i++) {
      sheet.cell(CellIndex.indexByString('A${8 + i}')).value = TextCellValue(
        summaryData[i][0].toString(),
      );
      sheet.cell(CellIndex.indexByString('A${8 + i}')).cellStyle = labelStyle;
      final value = summaryData[i][1];
      if (value is int) {
        sheet.cell(CellIndex.indexByString('B${8 + i}')).value = IntCellValue(
          value,
        );
      } else {
        sheet.cell(CellIndex.indexByString('B${8 + i}')).value = TextCellValue(
          value.toString(),
        );
      }
    }

    // Set column widths
    sheet.setColumnWidth(0, 25);
    sheet.setColumnWidth(1, 30);
  }

  static void _createGoalsSheet(Excel excel, SavingsReport report) {
    final sheet = excel['Detail Goals'];

    final headerStyle = CellStyle(
      bold: true,
      backgroundColorHex: ExcelColor.blue300,
      fontColorHex: ExcelColor.white,
    );

    // Headers
    final headers = [
      'No',
      'Nama Goal',
      'Tipe',
      'Target',
      'Terkumpul',
      'Sisa',
      'Progress',
      'Status',
      'Deadline',
      'Total Deposit',
    ];
    for (var i = 0; i < headers.length; i++) {
      sheet
          .cell(CellIndex.indexByColumnRow(columnIndex: i, rowIndex: 0))
          .value = TextCellValue(
        headers[i],
      );
      sheet
              .cell(CellIndex.indexByColumnRow(columnIndex: i, rowIndex: 0))
              .cellStyle =
          headerStyle;
    }

    // Data rows
    for (var i = 0; i < report.goals.length; i++) {
      final goal = report.goals[i];
      final row = i + 1;

      sheet
          .cell(CellIndex.indexByColumnRow(columnIndex: 0, rowIndex: row))
          .value = IntCellValue(
        i + 1,
      );
      sheet
          .cell(CellIndex.indexByColumnRow(columnIndex: 1, rowIndex: row))
          .value = TextCellValue(
        goal.name,
      );
      sheet
          .cell(CellIndex.indexByColumnRow(columnIndex: 2, rowIndex: row))
          .value = TextCellValue(
        goal.type,
      );
      sheet
          .cell(CellIndex.indexByColumnRow(columnIndex: 3, rowIndex: row))
          .value = DoubleCellValue(
        goal.targetAmount,
      );
      sheet
          .cell(CellIndex.indexByColumnRow(columnIndex: 4, rowIndex: row))
          .value = DoubleCellValue(
        goal.currentAmount,
      );
      sheet
          .cell(CellIndex.indexByColumnRow(columnIndex: 5, rowIndex: row))
          .value = DoubleCellValue(
        goal.remainingAmount,
      );
      sheet
          .cell(CellIndex.indexByColumnRow(columnIndex: 6, rowIndex: row))
          .value = TextCellValue(
        '${goal.progressPercentage.toStringAsFixed(1)}%',
      );
      sheet
          .cell(CellIndex.indexByColumnRow(columnIndex: 7, rowIndex: row))
          .value = TextCellValue(
        goal.status,
      );
      sheet
          .cell(CellIndex.indexByColumnRow(columnIndex: 8, rowIndex: row))
          .value = TextCellValue(
        goal.deadline ?? '-',
      );
      sheet
          .cell(CellIndex.indexByColumnRow(columnIndex: 9, rowIndex: row))
          .value = IntCellValue(
        goal.totalDeposits,
      );
    }

    // Set column widths
    sheet.setColumnWidth(0, 5);
    sheet.setColumnWidth(1, 25);
    sheet.setColumnWidth(2, 10);
    sheet.setColumnWidth(3, 18);
    sheet.setColumnWidth(4, 18);
    sheet.setColumnWidth(5, 18);
    sheet.setColumnWidth(6, 12);
    sheet.setColumnWidth(7, 12);
    sheet.setColumnWidth(8, 15);
    sheet.setColumnWidth(9, 12);
  }

  static void _createMonthlySheet(Excel excel, SavingsReport report) {
    final sheet = excel['Tren Bulanan'];

    final headerStyle = CellStyle(
      bold: true,
      backgroundColorHex: ExcelColor.orange300,
      fontColorHex: ExcelColor.white,
    );

    // Headers
    final headers = [
      'No',
      'Bulan',
      'Tahun',
      'Total Ditabung',
      'Jumlah Transaksi',
    ];
    for (var i = 0; i < headers.length; i++) {
      sheet
          .cell(CellIndex.indexByColumnRow(columnIndex: i, rowIndex: 0))
          .value = TextCellValue(
        headers[i],
      );
      sheet
              .cell(CellIndex.indexByColumnRow(columnIndex: i, rowIndex: 0))
              .cellStyle =
          headerStyle;
    }

    // Data rows
    for (var i = 0; i < report.monthlyBreakdown.length; i++) {
      final item = report.monthlyBreakdown[i];
      final row = i + 1;

      sheet
          .cell(CellIndex.indexByColumnRow(columnIndex: 0, rowIndex: row))
          .value = IntCellValue(
        i + 1,
      );
      sheet
          .cell(CellIndex.indexByColumnRow(columnIndex: 1, rowIndex: row))
          .value = TextCellValue(
        item.month,
      );
      sheet
          .cell(CellIndex.indexByColumnRow(columnIndex: 2, rowIndex: row))
          .value = IntCellValue(
        item.year,
      );
      sheet
          .cell(CellIndex.indexByColumnRow(columnIndex: 3, rowIndex: row))
          .value = DoubleCellValue(
        item.amount,
      );
      sheet
          .cell(CellIndex.indexByColumnRow(columnIndex: 4, rowIndex: row))
          .value = IntCellValue(
        item.depositCount,
      );
    }

    // Set column widths
    sheet.setColumnWidth(0, 5);
    sheet.setColumnWidth(1, 15);
    sheet.setColumnWidth(2, 10);
    sheet.setColumnWidth(3, 20);
    sheet.setColumnWidth(4, 18);
  }

  static void _createAchievementsSheet(Excel excel, SavingsReport report) {
    final sheet = excel['Pencapaian'];

    final headerStyle = CellStyle(
      bold: true,
      backgroundColorHex: ExcelColor.amber300,
      fontColorHex: ExcelColor.white,
    );

