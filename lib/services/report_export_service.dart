  static Future<File> exportToCsv(SavingsReport report) async {
    final StringBuffer buffer = StringBuffer();

    // Summary Header
    buffer.writeln('RINGKASAN TABUNGAN GOALMONEY');
    buffer.writeln('Tanggal Laporan,${report.reportDate}');
    buffer.writeln(
      'Periode,${report.period.startDate} - ${report.period.endDate}',
    );
    buffer.writeln();

    // Summary Data
    buffer.writeln('Item,Nilai');
    buffer.writeln('Total Goals,${report.summary.totalGoals}');
    buffer.writeln('Goals Selesai,${report.summary.completedGoals}');
    buffer.writeln('Total Ditabung,${report.summary.totalSaved}');
    buffer.writeln('Total Target,${report.summary.totalTarget}');
    buffer.writeln('Progress Keseluruhan,${report.summary.overallProgress}%');
    buffer.writeln();

    // Transactions Header
    buffer.writeln('RIWAYAT TRANSAKSI');
    buffer.writeln('Tanggal,Goal,Jumlah,Metode,Keterangan');

    for (final t in report.transactions) {
      buffer.writeln(
        '"${t.date}","${t.goalName}",${t.amount},"${t.method}","${t.description ?? "-"}"',
      );
    }

    final directory = await getApplicationDocumentsDirectory();
    final timestamp = DateFormat('yyyyMMdd_HHmmss').format(DateTime.now());
    final file = File('${directory.path}/Laporan_GoalMoney_$timestamp.csv');
    await file.writeAsString(buffer.toString());

    return file;
  }

